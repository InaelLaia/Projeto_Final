    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().isEmpty() || txtMarca.getText().isEmpty() || fmtPreco.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "Preencha todos os campos obrigatórios!");
                return;
        }
        
        Produto produto = new Produto();
        ProdutoDao dao = new ProdutoDao();
        
        try{
            
            double preco = Double.parseDouble(fmtPreco.getText());
            
            produto.setNome(txtNome.getText());
            produto.setMarca(txtMarca.getText());
            produto.setPreco(preco);
            
            dao.save(produto);
            
            JOptionPane.showMessageDialog(this, "Novo Produto cadastrado com sucesso!");
            
            TelaInicial inicio = new TelaInicial();
            inicio.setVisible(true);
            this.dispose();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Não foi possível inserir os dados! Por favor, verifique valores digitados!\n" + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

